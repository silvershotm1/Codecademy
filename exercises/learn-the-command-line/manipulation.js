
// CMD Line Manipulation

1. ``` ls -a ``` // -a is a "FLAG" on the ls command

2. ``` ls -alt ``` // you can combine multiple flags

3. ``` cp frida.txt lincoln.txt ``` // copy file to another name

4. ``` cp biopic/ray.txt biopic/notorious.txt historical/ ``` // copy txt into historical folder

5. ``` cp * satire/ ``` // copies everything into the satire folder

6. ``` cp m*.txt scifi/ ``` // any txt file starting with m to scifi folder

7. ``` mv superman.txt superhero/ ``` // moves files into superhero directory

8. ``` rm waterboy.txt ``` // removes files

9. ``` rm -r slapstick ``` // removes folder
