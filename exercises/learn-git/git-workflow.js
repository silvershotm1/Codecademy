
// Basic Git Workflow

1. ``` git init ``` // initializes new repo

2.  ```Git project with Working Directory, Staging Area and a repository.```

3. ``` git status ``` // track files and modifications
   ``` git add <filename> ``` // adds file to staging Area
   ``` git diff <filename> ``` // shows the changes in file
   ``` git commit -m "Complete first line of dialouge"``` // -m for msg to commit message
   ``` git log ``` // shows history of commits

   ``` git push origin <branch> ``` // pushes commits to branch names
