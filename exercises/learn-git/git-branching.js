
// Branching in Git

1. ``` git branch ``` // checks what current branch you are on.

2. ```git checkout -b fencing ``` // creates new branch fencing

3. ```git checkout fencing ``` // switches to branch fencing

4. ```git merge <branch-name> ``` // merges branch into current branch

5. ```git merge fencing-- ERR ``` // this merge gives error which needs to be fixed

6. ``` git branch -d <branch-name> ``` // this will delete the branch
