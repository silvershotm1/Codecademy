
// GIT TEAMWORK

1. ``` git clone science-quizzes my-quizzes ``` // clones repo to local machine.

2. ``` git remote -v ``` // list the remotes for the repo avail.

3. ``` git fetch ``` // doesn't merge, but pulls to local remote branch.

4. ``` git merge ``` // merges changes.

5. ``` git push origin/master ``` // pushes changes into master branch.
